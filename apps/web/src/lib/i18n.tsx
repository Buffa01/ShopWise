interface I18nContextValue {
  locale: Locale;
  setLocale: (locale: Locale) => void;
  t: (key: TranslationKey) => string;
}

const I18nContext = createContext<I18nContextValue | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>("es");

  useEffect(() => {
    const stored = window.localStorage.getItem(LOCALE_KEY);
    if (stored === "es" || stored === "en") {
      setLocaleState(stored);
    }
  }, []);

  const value = useMemo<I18nContextValue>(() => {
    return {
      locale,
      setLocale: (nextLocale) => {
        window.localStorage.setItem(LOCALE_KEY, nextLocale);
        setLocaleState(nextLocale);
      },
      t: (key) => dictionaries[locale][key]
    };
  }, [locale]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const value = useContext(I18nContext);

  if (!value) {
    throw new Error("useI18n must be used inside I18nProvider");
  }

  return value;
}

export function translateStatus(t: (key: TranslationKey) => string, status: string) {
  const key = `status.${status}` as TranslationKey;
  return key in dictionaries.es ? t(key) : status;
}

export function formatDate(locale: Locale, value: string) {
  return new Intl.DateTimeFormat(locale === "es" ? "es-UY" : "en-US").format(new Date(value));
}

export function formatDateTime(locale: Locale, value: string) {
  return new Intl.DateTimeFormat(locale === "es" ? "es-UY" : "en-US", {
    dateStyle: "short",
    timeStyle: "short"
  }).format(new Date(value));
}
